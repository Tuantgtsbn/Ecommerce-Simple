const mongoose = require("mongoose");
const Schema = mongoose.Schema;
const ContactSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
    read: {
      type: Boolean,
      default: false,
    },
  },
  {timestamps: true},
);

const ContactModel = mongoose.model("Contact", ContactSchema);
module.exports = ContactModel;
